// WHILE
var angka = 9

// Selama condition bernilai true
while(angka <= 3){
    console.log(angka);
    angka++
    // setelah baris terakhir di proses
    // akan check condition pada while
}

console.log('Selesai WHILE');

//  DO WHILE
var nilai = 9

do{
    console.log(nilai);
    nilai++
}while(nilai <= 3) // true

console.log('Selesai DO WHILE');